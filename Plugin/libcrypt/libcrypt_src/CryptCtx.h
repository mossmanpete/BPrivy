	typedef enum {
		CPHR_NULL = 0,
		CIPHER_BF_CBC = 1, // Blowfish in CBC mode
		CIPHER_AES_CBC     // AES (Rijndael) in CBC mode
	} CipherEnum;

	typedef enum {
		DEFAULT_KEY_LEN = 32 // Key Length in #bytes
	} CRYPT_CONSTANT;

	/*****************************************************************/
	/************************** CryptInfo ****************************/
	/*****************************************************************/
	struct CryptInfo
	{
		typedef enum {
			LOGN = 14,
			R = 8,
			P = 1,
			SALT_SIZE = SCRYPT_SALT_SIZE,
			SIG_SIZE = 32,
		} CONSTANT;

		/** Following are persisted to disk */
		size_t		m_logN;
		size_t		m_r;
		size_t		m_p;
		CipherEnum	m_cipher; // Cipher Enum
		size_t		m_keyLen; // Key Length in #bytes.
		Array<uint8_t, SALT_SIZE> m_salt;
		Array<uint8_t, SIG_SIZE>  m_signature;

		/** Following are ephemeral */
		const EVP_CIPHER*	m_EVP_CIPHER;
		size_t		m_ivLen;  // IV size in #bytes
		size_t		m_blkSize;// cipher's block size in #bytes

					CryptInfo		(CipherEnum cipher, size_t keyLen);
					CryptInfo		(const Buf<uint8_t>& cryptInfo);
					~CryptInfo		() {zero();}
		void		zero			();

	private:
					CryptInfo		(const CryptInfo&);// not to be defined
		CryptInfo&	operator=		(const CryptInfo&);// not to be defined
		void		ConstructCommon	(CipherEnum);
	};

	/*****************************************************************/
	/************************* CipherBlob ****************************/
	/*****************************************************************/
	struct CipherBlob
	{
	public:
					CipherBlob	(size_t tentativeDataSize=0);
					CipherBlob	(CipherBlob&& other);
		CipherBlob&	operator=	(CipherBlob&& other);
		void		zero ();
		void		pack();
		// Getters and Setters
		size_t		getSize				() {return m_encryptedSize+m_headerSize;}
		size_t		getEncryptedSize	() {return m_encryptedSize;}
		size_t		getHeaderSize		() {return m_headerSize;}
		void		putEncryptedSize	(size_t n) {m_encryptedSize = n;}
		uint8_t*	getDataBuf			() {return static_cast<uint8_t*>(m_buf) + m_headerSize;}
		BufHeap<uint8_t> m_buf;

	private:
		size_t		m_headerSize;
		size_t		m_encryptedSize;
		// Disabled operators
		CipherBlob(const CipherBlob&); // disabled
		CipherBlob& operator= (const CipherBlob&); // disabled
	};
	inline void CipherBlob::zero ()
	{
		m_headerSize = m_encryptedSize = 0;
		m_buf.zero();
	}
	inline
	CipherBlob::CipherBlob(CipherBlob&& other)
	  : m_buf(std::move(other.m_buf)),
		m_headerSize(other.m_headerSize),
		m_encryptedSize(other.m_encryptedSize)
	{
		other.m_headerSize = other.m_encryptedSize = 0;
	}
	inline CipherBlob&
	CipherBlob::operator=(CipherBlob&& other)
	{
		if (this == &other) return *this;
		m_headerSize = other.m_headerSize; other.m_headerSize = 0;
		m_encryptedSize = other.m_encryptedSize; other.m_encryptedSize = 0;
		m_buf = std::move(other.m_buf);
		return *this;
	}
